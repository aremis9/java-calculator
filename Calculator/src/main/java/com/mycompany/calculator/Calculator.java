    public void resetCalculator() {
        txtIO.setText("");
        lblOperation.setText("");
        num1 = 0;
        num2 = 0;
        operator = "";
        numInputState = 1;
        num1State = 1;
        
        txtIO.setFont(new Font("Arial", Font.PLAIN, 30));
    }
    
    public String numToStr(float n){
        String string = "";
        if (n % 1 == 0)
            string = String.valueOf((int) n);
        else
            string = String.valueOf(n);
        
        return string;
    }
    
    public void setOperator(String operation) {
        try {
            if (!txtIO.getText().equals("")) {
                if (num1State == 1) {
                    num1 = Float.parseFloat(txtIO.getText());
                    num1State = 2;
                }
                else {
                    if (numInputState == 1)
                        btnEqActionPerformed(null);
                    
                    num1 = Float.parseFloat(txtIO.getText());
                }
            }
            
            operator = operation;
            num2 = num1;
            
            lblOperation.setText(numToStr(num1) + " " + operator + " ");
            numInputState = 2;
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
    public void btnNumAction(String num, boolean fromBtn) {
        if (numInputState == 2) {
            txtIO.setText("");
        }
        numInputState = 1;
        
        if (fromBtn)
            txtIO.setText(txtIO.getText() + num);
    }
    
