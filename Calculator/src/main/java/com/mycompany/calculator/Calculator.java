    private void btnEqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqActionPerformed
        // TODO add your handling code here:
        if (numInputState == 1) {
            try {
                num2 = Float.parseFloat(txtIO.getText());
            } catch (Exception e) {
                System.out.println(e);
            }
        }
        else if (numInputState == 2) {
            try {
                num1 = Float.parseFloat(txtIO.getText());
            } catch (Exception e) {
                num1 = 0;
                System.out.println(e);
            }
        }
        
        
        String ans = "";
        if (operator == "+")
            ans = numToStr(num1 + num2);
        else if (operator == "-")
            ans = numToStr(num1 - num2);
        else if (operator == "×")
            ans = numToStr(num1 * num2);
        else if (operator == "÷") {
            if (num2 == 0) {
                // Division by zero
                ActionListener listener = new ActionListener(){
                    public void actionPerformed(java.awt.event.ActionEvent evt){
                        resetCalculator();
                    }
                };
                
                ans = "Cannot divide by zero.";
                txtIO.setText(ans);
                txtIO.setFont(new Font("Arial", Font.PLAIN, 18));
                System.out.println(txtIO.getText());
                
                Timer timer = new Timer(2000, listener);
                timer.setRepeats(false);
                timer.start();
                
                return;
            }
            ans = numToStr(num1 / num2);
        }
        else {
            try {
                // Clicked equals without operation -> ans = first operand
                num1 = Float.parseFloat(txtIO.getText());
                lblOperation.setText(numToStr(num1) + " =");
                txtIO.setText(numToStr(num1));  
            } catch (Exception e) {
                // Clicked equals without operation and operands
                num1 = 0;
                lblOperation.setText(numToStr(num1) + " =");
                txtIO.setText(numToStr(num1));  
                
                System.out.println(e);
            }
            
            numInputState = 1;
            return;
        }
        
        String num1_s = numToStr(num1);
        String num2_s = numToStr(num2);
        lblOperation.setText(num1_s + " " + operator + " " + num2_s + " =");
        txtIO.setText(ans);
        numInputState = 2;
        
    }//GEN-LAST:event_btnEqActionPerformed

