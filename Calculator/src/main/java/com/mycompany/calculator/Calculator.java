    private void txtIOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIOKeyTyped
        // TODO add your handling code here:
        char key = evt.getKeyChar();
        
        if (key == '+')
            setOperator("+");
        else if (key == '-')
            setOperator("-");
        else if (key == '*')
            setOperator("×");
        else if (key == '/')
            setOperator("÷");
        else if (key == '\n') 
            btnEqActionPerformed(null);
        
        if(!(Character.isDigit(key)))
            evt.consume();
        else
            btnNumAction(Character.toString(key), false);
    }//GEN-LAST:event_txtIOKeyTyped

